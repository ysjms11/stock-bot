def _is_us_market_hours_kst() -> bool:
    """미국 장 시간 여부 (KST 기준: 월~금 23:30~06:00)"""
    now = datetime.now(KST)
    wd, h, m = now.weekday(), now.hour, now.minute
    if h == 23 and m >= 30:
        return wd < 5          # 월~금 밤 KST → 미국 장중
    if h < 6:
        return 1 <= wd <= 5    # 화~토 새벽 KST (전날 미국 장중)
    return False

